/**
 * Agent report CRUD query helpers.
 *
 * All functions obtain the SQLite instance internally via `getDatabase()`.
 * Queries use positional `?` placeholders throughout (better-sqlite3).
 */

import { getDatabase } from '@myco/db/client.js';

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

/** Default number of reports returned by list queries when no limit given. */
const DEFAULT_LIST_LIMIT = 100;

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/** Fields required (or optional) when inserting a report. */
export interface ReportInsert {
  run_id: string;
  agent_id: string;
  action: string;
  summary: string;
  details?: string | null;
  created_at: number;
}

/** Row shape returned from agent_reports queries (all columns). */
export interface ReportRow {
  id: number;
  run_id: string;
  agent_id: string;
  action: string;
  summary: string;
  details: string | null;
  created_at: number;
}

/** Filter options for `listReportsByAgent`. */
export interface ListReportsByAgentOptions {
  limit?: number;
}

// ---------------------------------------------------------------------------
// Column list
// ---------------------------------------------------------------------------

const REPORT_COLUMNS = [
  'id',
  'run_id',
  'agent_id',
  'action',
  'summary',
  'details',
  'created_at',
] as const;

const SELECT_COLUMNS = REPORT_COLUMNS.join(', ');

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Normalize a SQLite result row into a typed ReportRow. */
function toReportRow(row: Record<string, unknown>): ReportRow {
  return {
    id: row.id as number,
    run_id: row.run_id as string,
    agent_id: row.agent_id as string,
    action: row.action as string,
    summary: row.summary as string,
    details: (row.details as string) ?? null,
    created_at: row.created_at as number,
  };
}

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/**
 * Insert a new agent report.
 *
 * The `id` is auto-generated by the INTEGER PRIMARY KEY (AUTOINCREMENT).
 */
export function insertReport(data: ReportInsert): ReportRow {
  const db = getDatabase();

  const info = db.prepare(
    `INSERT INTO agent_reports (
       run_id, agent_id, action, summary, details, created_at
     ) VALUES (
       ?, ?, ?, ?, ?, ?
     )`,
  ).run(
    data.run_id,
    data.agent_id,
    data.action,
    data.summary,
    data.details ?? null,
    data.created_at,
  );

  const reportId = Number(info.lastInsertRowid);

  return toReportRow(
    db.prepare(`SELECT ${SELECT_COLUMNS} FROM agent_reports WHERE id = ?`).get(reportId) as Record<string, unknown>,
  );
}

/**
 * List all reports for a specific run, ordered by created_at ASC.
 */
export function listReports(runId: string): ReportRow[] {
  const db = getDatabase();

  const rows = db.prepare(
    `SELECT ${SELECT_COLUMNS}
     FROM agent_reports
     WHERE run_id = ?
     ORDER BY created_at ASC`,
  ).all(runId) as Record<string, unknown>[];

  return rows.map(toReportRow);
}

/**
 * List reports by agent, ordered by created_at DESC.
 */
export function listReportsByAgent(
  agentId: string,
  options: ListReportsByAgentOptions = {},
): ReportRow[] {
  const db = getDatabase();

  const limit = options.limit ?? DEFAULT_LIST_LIMIT;

  const rows = db.prepare(
    `SELECT ${SELECT_COLUMNS}
     FROM agent_reports
     WHERE agent_id = ?
     ORDER BY created_at DESC
     LIMIT ?`,
  ).all(agentId, limit) as Record<string, unknown>[];

  return rows.map(toReportRow);
}
